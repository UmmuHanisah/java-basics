package increment.decrement;

/**
 *
 * @author Ummu Hanisah
 */
public class IncrementThenewboston {
    public static void main(String[] args) {
        
        int tuna = 5;
        System.out.println(tuna++);
        System.out.println(tuna);
        /*Output
        ######
        5
        6
        */
        
        int cake = 5;
        cake = cake + 5;
        System.out.println(cake);
        /*Output
        ######
        10
        */
        
        int tomyam = 5;
        tomyam += 8;
        System.out.println(tomyam);
        /*Output
        ######
        13
        */
    }
    
}
